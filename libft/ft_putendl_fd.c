#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	a;

	if (s == 0)
		return ;
	a = '\n';
	while (*s != '\0')
	{
		write(fd, s, 1);
		s++;
	}
	write(fd, &a, 1);
}
